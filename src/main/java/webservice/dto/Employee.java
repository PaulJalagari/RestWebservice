package webservice.dto;

public class Employee {
	int empid;
	String emp_timestamp;

	public int getEmpid() {
		return empid;
	}

	public void setEmpid(int empid) {
		this.empid = empid;
	}

	public String getEmp_timestamp() {
		return emp_timestamp;
	}

	public void setEmp_timestamp(String emp_timestamp) {
		this.emp_timestamp = emp_timestamp;
	}

}
